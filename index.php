<h3> Land Pollution</h3>
<p>Land pollution is the cause of many different factors that ultimately pollute the land. These factors include solid waste, deforestation, chemical, and agricultural activities. Many of these cause a bad affect on the environment.
Solid waste mostly made up of products that are non-biodegradable and these are hard to dispose of.
Deforestation causes the loss of the topmost fertile layer of soil that is essential for the growth of plants and trees. Chemicals are hard in nature and difficult to dispose of. Besides, the solid waste of pesticides, insecticides, and fertilizers are either thrown in landfills or at other places. These wastes create another type of land pollution. In addition, agricultural activities use a high amount of chemical products that not only harm the food crop but also the land. Furthermore, these cause other pollution too.</p>
</td>

